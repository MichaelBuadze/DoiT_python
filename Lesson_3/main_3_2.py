#2. დაწერეთ პითონის პროგრამა, რომელიც მიიღებს მომხმარებლისგან რიცხვს და შემდეგ იყენებს "while" 
# ციკლს რომ რევესრულად დაბეჭდოს რიცხვები 0-მდე. მაგალითად თუ შეიყვანს 4, დაიბეჭდოს 4, 3, 2, 1

# რიცხვის შეყვანა მომხმარებლისგან
num = int(input("\nშეიყვანეთ რიცხვი: "))

# while ციკლი
while num > 1:
    print(num, end=",")
    num -= 1
print(num, end=" ")

# მეთოდი for range:

n = int(input("\nშეიყვანეთ ნებისმიერი რიცხვი: "))
for i in range(n, 0, -1):
    print(i, end=" ")