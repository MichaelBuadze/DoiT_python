# ეს ფუნქცია ანგარიშობს, კონსოლიდან მიღებული, დადებითი მთელი რიცხვის შემადგენელ ციფრთა ჯამს.
def calc_digit_sum(num):
    # წარმოვადგინოთ sum ცვლადი 
    sum = 0  
    # while ციკლი ...              
    while num > 0:
        digit = num % 10  # % ნაშთის ოპერაციით, რიცხვის 10-ზე გაყოფის შედეგად მივიღებთ ერთეულს
        sum += digit      # sum = sum + digit
        num //= 10        # number = number // 10 - მთელზე გაყოფის ოპერაციით მივიღებთ ათეულებამდე დამრგვალებულ რიცხვს
    return sum            # ფუნქცია აგროვებს sum  
# ... ფუნქციის განსაზვრის დასასრული 

