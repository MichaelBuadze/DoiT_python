# შეყვანილი ინფორმაციის ვალიდაცია ...
while True:
    number = input("\nშეიყვანეთ მთელი რიცხვი:  ")
    if number.isdigit():
        #თუ შეყვანილი, დადებითი მთელი რიცხვია ... 
        num = int(number)
        break
    else:
       print("\nგთხოვთ შეიყვანოთ დადებითი მთელი რიცხვი. \n")
# შეყვანილი ინფორმაციის ვალიდაცია ...
                       
result = calc_digit_sum(num)

print (f"\n{num}-ის შემადგენელ ციფრთა ჯამია:  {result}")
